    @Override
    public boolean checkPanel() {

        if (!checkBoxPMN.isSelected()
                & !checkBoxPPL.isSelected()
                & !checkBoxPRT.isSelected()
                & !checkBoxPSL.isSelected()) {
            JOptionPane.showMessageDialog(
                    this,
                    "Выберите хотябы один тип операции для отображения",
                    "Внимание...",
                    JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }

