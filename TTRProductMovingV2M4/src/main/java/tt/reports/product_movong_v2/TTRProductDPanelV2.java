    private class UserItem {
        private int id;
        private String userName;

        public UserItem(int id, String userName) {
            this.id = id;
            this.userName = userName;
        }

        public int getId() {
            return id;
        }

        public String getUserName() {
            return userName;
        }

        @Override
        public String toString() {
            return this.userName;
        }


    }

    /** Creates new form ProductDPanelV2 */
    public TTRProductDPanelV2(PGPoolingDataSource source) {

        initComponents();
        beginDatePicker.setDate(new Date());
        endDatePicker.setDate(new Date());

        List<UserItem> users = new ArrayList<UserItem>();

        PADBResult result = null;
        try {

            ADBProc proc = new ADBProc("users_select_for_loginer");
            result = PADBUtils.getResultSet(source, proc);
            ResultSet rs = result.getRs();
            while (rs.next()) {
                users.add(new UserItem(rs.getInt("id"), rs.getString("name")));
            }

        } catch (SQLException ex) {
            Logger.getLogger(TTRProductDPanelV2.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            result.close();
        }
        DefaultComboBoxModel bcbm = new DefaultComboBoxModel(users.toArray());

        usersComboBox.setModel(bcbm);
    }

