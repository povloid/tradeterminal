    @Action
    public void selectPGroup() {

        ProgectsGroupsMainPanel p = new ProgectsGroupsMainPanel();
        AUniversalAddDialog d = new AUniversalAddDialog(p, null, true);
        d.setTitle("Группа");
        d.setTitleText("Группа");

        d.setVisible(true);
        d.dispose();

        if (d.getReturnStatus() == ADialog.RET_OK) {
            groupId = p.getSelectedGroupId();
            selectedGroupLabel.setText(p.getSelectedGroupName());

            if (plusGroupName) {
                pPanel.set_Name(p.getSelectedGroupName() + " - " + pPanel.get_Name());
            }

        }
    }

    @Override
    public boolean checkPanel() {

        // Проверка правильности выбора группы
        if (groupId == -1) {
            JOptionPane.showMessageDialog(
                    this,
                    "Выберите группу товара.",
                    "Информация",
                    JOptionPane.INFORMATION_MESSAGE);

            selectPGroupButton.requestFocus();

            return false;
        }

        // Проверка заполнения товара

        if (!pPanel.checkPanel()) {
            return false;
        }


        ADBProc proc = new ADBProc("products_insert");

        proc.addInParametr(new ADBProcParametr(Types.INTEGER, groupId));

        proc.addInParametr(new ADBProcParametr(Types.VARCHAR, pPanel.get_Name()));
        proc.addInParametr(new ADBProcParametr(Types.VARCHAR, pPanel.getDescription()));
        proc.addInParametr(new ADBProcParametr(Types.VARCHAR, pPanel.getScod()));
        proc.addInParametr(new ADBProcParametr(Types.INTEGER, pPanel.getMeasures_id()));

        proc.addInParametr(new ADBProcParametr(Types.NUMERIC, pPanel.getList_price()));
        proc.addInParametr(new ADBProcParametr(Types.NUMERIC, pPanel.getSpec_price()));
        proc.addInParametr(new ADBProcParametr(Types.NUMERIC, new BigDecimal(((Double) pPanel.getPercent_discount()) * 0.01f)));

        productId = PADBUtils.getIntScalar(Setup.getSource(), proc);

        productSCod = pPanel.getScod();

        return true;

    }

